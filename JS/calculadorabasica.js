
//Se utilza parseFloat para reconocer que son números y no cadena de texto
let primeravariable = parseFloat(prompt ("Ingrese un número: "));
let segundavariable = parseFloat(prompt("Ingrese otro número:"));

let suma = primeravariable + segundavariable;
let resta = primeravariable - segundavariable;
let division = primeravariable / segundavariable;
let multiplicacion = primeravariable * segundavariable;

console.log (`La Suma de los números es: ${suma}`);
console.log (`La Resta de los números es: ${resta}`);
console.log (`La División de los números es: ${division}`);
console.log (`La Multiplicación de los números es: ${multiplicacion}`);